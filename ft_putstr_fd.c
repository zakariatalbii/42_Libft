#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	v;

	if (fd < 0 || s == NULL)
		return ;
	v = 0;
	while (s[v] != '\0')
		ft_putchar_fd(s[v++], fd);
}
